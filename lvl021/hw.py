#2) შექმენით სია და შეიტანეთ ასოები, slicing-ის გამოყენებით გამოიტანეთ:
# პირველი 5 ასო, ბოლო 3 ასო, უკუღმა და მე-2 დან მე-6 ამდე

array = ['k', 'a', 's', 'p', 'e', 'r', 'i', 't', 'o']

print(array[:5])
print(array[-3:])
print(array[::-1])
print(array[-6:-1])


#შექმენით სია და შეიტანეთ ციფრები და slicing-ის გამოყენებით გამოიტანეთ:
